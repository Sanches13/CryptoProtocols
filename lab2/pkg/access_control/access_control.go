package access_control

import (
	"bytes"
	"kdf_gostr/pkg/streebog"
)

func CheckAccessControl(user_password string) bool {
	passwd := streebog.NewHash()
	passwd.Write([]byte(user_password))

	// password
	if !bytes.Equal(passwd.Sum(nil), []byte{0x56, 0x8c, 0x15, 0x0c, 0xc0, 0xd9, 0xa0, 0x06,
		0xcf, 0x9c, 0x4f, 0x28, 0x0a, 0x42, 0x76, 0x86,
		0xe8, 0xf4, 0x6c, 0x54, 0x3a, 0xda, 0x3d, 0xab,
		0xe8, 0x31, 0x99, 0xdd, 0x8f, 0xa8, 0x21, 0x41}) {
		return false
	} else {
		return true
	}
}
